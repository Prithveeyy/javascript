var grade = "A";
switch (grade) {
    case "A":
        console.log("Excellent");
        break;
    case "B":
        console.log("Good");
        break;
    case "C":
        console.log("Fair");
        break;
    case "D":
        console.log("Needs Improvement");
        break;
    default:
        console.log("Invalid Grade");
}
